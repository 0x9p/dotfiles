    cp "$task_file" "${task_file}.bak"
    
    # Uncheck all status checkboxes
    sed -i .bak 's/^- \[x\] â³ \*\*TODO\*\*/- [ ] â³ **TODO**/' "$task_file" && rm -f "${task_file}.bak"
