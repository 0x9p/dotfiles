log_info() {
    echo -e "${BLUE}$1${NC}"
}

log_success() {
    echo -e "${GREEN}$1${NC}"
}

log_warning() {
    echo -e "${YELLOW}$1${NC}"
}

log_error() {
    echo -e "${RED}$1${NC}"
}

prompt_user() {
    local prompt_text="$1"
    local var_name="$2"
    
    echo -e "${YELLOW}${prompt_text}${NC}"
    read -e -p "> " "$var_name"
}

expand_path() {
    local path="$1"
    echo "${path/#\~/$HOME}"
}

# ============================================
